package JuegoDeCartas;

public class Caracteristica {

	private String nombre;
	private int valor;
	
	public Caracteristica(String nombre, int valor) {
		this.nombre = nombre;
		this.valor = valor;
	}

	public String getNombre() {
		return nombre;
	}

	public int getValor() {
		return valor;
	}
	
	
}
